#include<iostream>

using namespace std;

int main(){
    int num = 5;
    cout << "Before incrementing: " << num << endl;
    cout << "Using num++: " << num++ << endl;
    cout << "After using num++: " << num << endl;
    cout << "Using ++num: " << ++num << endl;
    cout << "After using ++num: " << num << endl;
    return 0;
}

